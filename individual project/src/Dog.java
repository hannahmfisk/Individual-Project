public class Dog 

{


int idNumber;

String name;

String gender;

int age;

String breed;


public Dog(int id, String nm, String gndr, int ag, String brd)

{

idNumber = id;

name = nm;

gender = gndr;

age = ag;

breed = brd;

}


public int getAge()

{

return age;

}

public String getGender()

{

return gender;

}

public String getName()

{

return name;

}









}